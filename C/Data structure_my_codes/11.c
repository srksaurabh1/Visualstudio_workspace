// value in Decimal, Octal, Hexadecimal Formats
#include<stdio.h>

int main()
{   
    int a= 12;
    printf("Decimal number : %d\n",a);
    printf("Octal number : %o\n",a);
    printf("Hexadecimal number : %x\n",a);
    return 0;
}
//     %d - integer format.
//     %o - octal format.
//     %x - hexadecimal format (lowercase)
//     %X - hexadecimal format (UPPERCASE)
